package U2_Variables;
/**
 * Ejercicio 2.4 - Área de rectángulo
 * Enunciado: Usa variables para base=5 y altura=8 y muestra el área del rectángulo.
 */
public class AreaRectangulo {
  public static void main(String[] args) {
    int base = 5;
    int altura = 8;
    int area = base * altura;
    System.out.println("Área = " + area);
  }
}
