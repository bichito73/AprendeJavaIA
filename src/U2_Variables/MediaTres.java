package U2_Variables;
/**
 * Ejercicio 2.10 - Media aritmética
 * Enunciado: Declara tres números y muestra su media.
 */
public class MediaTres {
  public static void main(String[] args) {
    double a = 5, b = 7, c = 9;
    double media = (a + b + c) / 3.0;
    System.out.println("Media: " + media);
  }
}
