package U2_Variables;
/**
 * Ejercicio 2.2 - Datos personales en variables
 * Enunciado: Guarda en variables tu nombre, dirección y teléfono y muéstralos.
 */
public class DatosEnVariables {
  public static void main(String[] args) {
    String nombre = "Maria del Carmen";
    String direccion = "Calle Ejemplo 123, Aguadulce";
    String telefono = "600123456";
    System.out.println("Nombre: " + nombre);
    System.out.println("Dirección: " + direccion);
    System.out.println("Teléfono: " + telefono);
  }
}
